-- Order to Delivery flow
INSERT INTO SAP.SAP_ECC.VBFA (MANDT, VBELV, POSNV, VBELN, POSNN, VBTYP_N, ERDAT)
WITH order_del_map AS (
    SELECT d.MANDT, s.ORD_VBELN, d.DEL_VBELN, d.RN
    FROM (
        SELECT l.VBELN AS DEL_VBELN, l.MANDT, l.KUNNR, l.WADAT,
               ROW_NUMBER() OVER (ORDER BY l.VBELN) as RN
        FROM SAP.SAP_ECC.LIKP l
    ) d
    JOIN (
        SELECT v.VBELN AS ORD_VBELN, v.MANDT, v.KUNNR,
               ROW_NUMBER() OVER (PARTITION BY v.KUNNR ORDER BY v.ERDAT) as RN
        FROM SAP.SAP_ECC.VBAK v
        JOIN SAP.SAP_ECC.VBUK u ON v.MANDT = u.MANDT AND v.VBELN = u.VBELN
        WHERE u.LFSTK IN ('B', 'C')
    ) s ON d.MANDT = s.MANDT AND d.KUNNR = s.KUNNR AND d.RN = s.RN
)
SELECT m.MANDT, m.ORD_VBELN, '000010', m.DEL_VBELN, '000010', 'J', CURRENT_DATE()
FROM order_del_map m
LIMIT 100000;

