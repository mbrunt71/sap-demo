-- ============================================================================
-- GENERATE SAP SYSTEM - SAP.SAP_ECC
-- Creates SAP ECC tables with primary/foreign keys and generates
-- 100,000 orders with consistent sample data across all related tables
-- ============================================================================

USE ROLE ACCOUNTADMIN;
CREATE DATABASE IF NOT EXISTS SAP;
CREATE SCHEMA IF NOT EXISTS SAP.SAP_ECC;
USE SCHEMA SAP.SAP_ECC;

-- ============================================================================
-- MASTER DATA TABLES
-- ============================================================================

-- T001W: Plants
CREATE OR REPLACE TABLE SAP.SAP_ECC.T001W (
    MANDT VARCHAR(3) DEFAULT '800',
    WERKS VARCHAR(4) NOT NULL,
    NAME1 VARCHAR(30),
    BUKRS VARCHAR(4),
    LAND1 VARCHAR(3),
    CONSTRAINT PK_T001W PRIMARY KEY (MANDT, WERKS)
);

-- KNA1: Customer Master (General)
CREATE OR REPLACE TABLE SAP.SAP_ECC.KNA1 (
    MANDT VARCHAR(3) DEFAULT '800',
