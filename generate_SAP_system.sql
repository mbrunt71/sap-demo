-- Billing Items (VBRP)
INSERT INTO SAP.SAP_ECC.VBRP (MANDT, VBELN, POSNR, MATNR, FKIMG, VRKME, NETWR)
SELECT b.MANDT, b.VBELN, li.POSNR, li.MATNR, li.LFIMG, li.VRKME,
