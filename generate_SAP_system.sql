        WHEN 0 THEN '1000' WHEN 1 THEN '2000' WHEN 2 THEN '3000' WHEN 3 THEN '4000' ELSE '5000'
    END
FROM (
    SELECT o.SEQ AS ORD_NUM, i.SEQ AS ITEM_NUM
    FROM (SELECT SEQ4() + 1 AS SEQ FROM TABLE(GENERATOR(ROWCOUNT => 100000))) o
    CROSS JOIN (SELECT SEQ4() + 1 AS SEQ FROM TABLE(GENERATOR(ROWCOUNT => 5))) i
    WHERE i.SEQ <= GREATEST(1, MOD(ABS(HASH(o.SEQ::VARCHAR)), 5) + 1)
);

