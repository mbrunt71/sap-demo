-- Correct LIPS generation: link delivery items to order items via VBFA flow
TRUNCATE TABLE SAP.SAP_ECC.LIPS;
INSERT INTO SAP.SAP_ECC.LIPS (MANDT, VBELN, POSNR, MATNR, LFIMG, VRKME, WERKS)
WITH delivery_orders AS (
    SELECT l.VBELN AS DEL_VBELN, l.MANDT, l.KUNNR,
           ROW_NUMBER() OVER (ORDER BY l.VBELN) as RN
    FROM SAP.SAP_ECC.LIKP l
),
source_orders AS (
    SELECT v.VBELN AS ORD_VBELN, v.MANDT, v.KUNNR,
           ROW_NUMBER() OVER (PARTITION BY v.KUNNR ORDER BY v.ERDAT) as RN
    FROM SAP.SAP_ECC.VBAK v
    JOIN SAP.SAP_ECC.VBUK u ON v.MANDT = u.MANDT AND v.VBELN = u.VBELN
    WHERE u.LFSTK IN ('B', 'C')
)
SELECT d.MANDT, d.DEL_VBELN, ap.POSNR, ap.MATNR, ap.KWMENG, ap.VRKME, ap.WERKS
FROM delivery_orders d
JOIN source_orders s ON d.MANDT = s.MANDT AND d.KUNNR = s.KUNNR AND d.RN = s.RN
JOIN SAP.SAP_ECC.VBAP ap ON s.MANDT = ap.MANDT AND s.ORD_VBELN = ap.VBELN;

