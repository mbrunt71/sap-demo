
  
    



create or replace transient  table DEMO.SILVER.material_valuation
    
    
    
    as (SELECT
    MANDT AS CLIENT,
    MATNR AS MATERIAL_NUMBER,
    BWKEY AS VALUATION_AREA,
    BWTAR AS VALUATION_TYPE,
    VPRSV AS PRICE_CONTROL,
    VERPR AS MOVING_AVG_PRICE,
    STPRS AS STANDARD_PRICE,
    PEINH AS PRICE_UNIT,
    SALK3 AS TOTAL_VALUE,
    LBKUM AS TOTAL_STOCK,
    BKLAS AS VALUATION_CLASS,
    MLAST AS ML_ACTIVE,
    LAEPR AS LAST_PRICE_CHANGE_DATE
FROM DEMO.SAP_ECC.MBEW
    )
;




  