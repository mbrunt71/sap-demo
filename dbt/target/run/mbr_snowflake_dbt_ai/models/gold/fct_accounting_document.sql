
  create or replace   view DEMO.SILVER_GOLD_DIMENSIONAL.fct_accounting_document
  
  
  
  
  as (
    SELECT
    h.COMPANY_CODE,
    h.DOCUMENT_NUMBER,
    h.FISCAL_YEAR,
    h.DOCUMENT_TYPE,
    h.POSTING_DATE,
    h.DOCUMENT_DATE,
    h.CURRENCY,
    i.LINE_ITEM,
    i.ACCOUNT_TYPE,
    i.DEBIT_CREDIT_INDICATOR,
    i.LOCAL_CURRENCY_AMOUNT,
    i.DOCUMENT_CURRENCY_AMOUNT,
    i.GL_ACCOUNT,
    i.COST_CENTER,
    i.MATERIAL_NUMBER
FROM DEMO.SILVER_SILVER.accounting_document_headers h
JOIN DEMO.SILVER_SILVER.accounting_document_items i
    ON h.COMPANY_CODE = i.COMPANY_CODE
    AND h.DOCUMENT_NUMBER = i.DOCUMENT_NUMBER
    AND h.FISCAL_YEAR = i.FISCAL_YEAR
  );

